package com.tmobile.wrapper.model;

public class ServiceStatusInfo {
	
	public String serviceStatus;
	public String serviceName;
	public String fallbackHandled;
	public String message;
	public String serviceHost;
	
	public String getServiceStatus() {
		return serviceStatus;
	}
	public void setServiceStatus(String serviceStatus) {
		this.serviceStatus = serviceStatus;
	}
	public String getServiceName() {
		return serviceName;
	}
	public void setServiceName(String serviceName) {
		this.serviceName = serviceName;
	}
	public String getFallbackHandled() {
		return fallbackHandled;
	}
	public void setFallbackHandled(String fallbackHandled) {
		this.fallbackHandled = fallbackHandled;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	public String getServiceHost() {
		return serviceHost;
	}
	public void setServiceHost(String serviceHost) {
		this.serviceHost = serviceHost;
	}
	
	
	

}
